# Ejemplos utilizando operadores aritmeticos
numero1 = int(input("Ingrese el numero1: "))
numero2 = int(input("Ingrese el numero2: "))
suma = numero1 + numero2
print(f"La suma es {numero1} y {numero2}",str(suma))
#RESTA
resta = numero1 - numero2
print(f"La RESTA es {numero1} y {numero2}",str(resta))
#DIVISION
DIVISION = numero1 / numero2
print(f"La DIVISION es {numero1} y {numero2}",str(DIVISION))
#MULTIPLICACION
MULTIPLICACION = numero1 - numero2
print(f"La MULTIPLICACION es {numero1} y {numero2}",str(MULTIPLICACION))
